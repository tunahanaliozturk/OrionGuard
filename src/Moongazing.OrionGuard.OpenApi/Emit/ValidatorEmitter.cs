#nullable enable

using System.Collections.Generic;
using System.Globalization;
using System.Text;
using Moongazing.OrionGuard.OpenApi.Json;
using Moongazing.OrionGuard.OpenApi.Model;

namespace Moongazing.OrionGuard.OpenApi.Emit
{
    /// <summary>
    /// Emits the C# source for an OrionGuard validator derived from a resolved OpenAPI schema.
    /// </summary>
    /// <remarks>
    /// <para>
    /// The generated type is a partial class that implements
    /// <c>Moongazing.OrionGuard.DependencyInjection.IValidator&lt;T&gt;</c> and returns a real
    /// <c>Moongazing.OrionGuard.Core.GuardResult</c>, so a generated validator is interchangeable with a
    /// hand-written one anywhere an <c>IValidator&lt;T&gt;</c> is consumed (ASP.NET Core filter, MediatR
    /// behaviour, manual call).
    /// </para>
    /// <para>
    /// Every constraint is gated on the validated member's <see cref="MemberTypeCategory"/>: a string
    /// length check is only emitted for a string member, a numeric range only for a numeric member, and
    /// so on. This keeps the emitted code compiling under the consumer's <c>TreatWarningsAsErrors</c>
    /// even when the document and the POCO disagree about a property's shape.
    /// </para>
    /// </remarks>
    internal static class ValidatorEmitter
    {
        private const string GuardResult = "global::Moongazing.OrionGuard.Core.GuardResult";
        private const string ValidationError = "global::Moongazing.OrionGuard.Core.ValidationError";
        private const string IValidator = "global::Moongazing.OrionGuard.DependencyInjection.IValidator";
        private const string Regex = "global::System.Text.RegularExpressions.Regex";

        public static string Emit(ValidatorEmitModel model)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable enable");
            sb.AppendLine();

            bool hasNamespace = !string.IsNullOrEmpty(model.Namespace);
            string indent = hasNamespace ? "    " : string.Empty;

            if (hasNamespace)
            {
                sb.Append("namespace ").AppendLine(model.Namespace);
                sb.AppendLine("{");
            }

            sb.Append(indent).AppendLine("/// <summary>");
            sb.Append(indent).Append("/// OpenAPI-derived validator for <see cref=\"")
                .Append(model.ValidatedTypeFullName).AppendLine("\"/>.");
            sb.Append(indent).AppendLine("/// Generated from the referenced OpenAPI schema; do not edit by hand.");
            sb.Append(indent).AppendLine("/// </summary>");
            sb.Append(indent).Append("public partial class ").Append(model.ClassName)
                .Append(" : ").Append(IValidator).Append('<').Append(model.ValidatedTypeFullName).AppendLine(">");
            sb.Append(indent).AppendLine("{");

            EmitValidateMethod(sb, model, indent + "    ");
            sb.AppendLine();
            EmitValidateAsyncMethod(sb, model, indent + "    ");

            sb.Append(indent).AppendLine("}");

            if (hasNamespace)
            {
                sb.AppendLine("}");
            }

            return sb.ToString();
        }

        private static void EmitValidateMethod(StringBuilder sb, ValidatorEmitModel model, string indent)
        {
            string t = model.ValidatedTypeFullName;

            sb.Append(indent).AppendLine("/// <summary>");
            sb.Append(indent).AppendLine("/// Validates the instance against the OpenAPI schema constraints.");
            sb.Append(indent).AppendLine("/// </summary>");
            sb.Append(indent).Append("public ").Append(GuardResult).Append(" Validate(").Append(t).AppendLine(" value)");
            sb.Append(indent).AppendLine("{");

            string body = indent + "    ";
            sb.Append(body).Append("var __errors = new global::System.Collections.Generic.List<")
                .Append(ValidationError).AppendLine(">();");
            sb.Append(body).AppendLine("if (value is null)");
            sb.Append(body).AppendLine("{");
            sb.Append(body).Append("    __errors.Add(new ").Append(ValidationError)
                .AppendLine("(\"\", \"The value cannot be null.\", \"NULL\"));");
            sb.Append(body).Append("    return ").Append(GuardResult).AppendLine(".Failure(__errors);");
            sb.Append(body).AppendLine("}");
            sb.AppendLine();

            foreach (var property in model.Properties)
            {
                EmitProperty(sb, property, body);
            }

            sb.Append(body).Append("return __errors.Count == 0 ? ").Append(GuardResult)
                .Append(".Success() : ").Append(GuardResult).AppendLine(".Failure(__errors);");
            sb.Append(indent).AppendLine("}");
        }

        private static void EmitValidateAsyncMethod(StringBuilder sb, ValidatorEmitModel model, string indent)
        {
            string t = model.ValidatedTypeFullName;

            sb.Append(indent).AppendLine("/// <summary>");
            sb.Append(indent).AppendLine("/// Asynchronous entry point. The generated schema rules are synchronous,");
            sb.Append(indent).AppendLine("/// so this simply wraps <see cref=\"Validate\"/> for the IValidator contract.");
            sb.Append(indent).AppendLine("/// </summary>");
            sb.Append(indent)
                .Append("public global::System.Threading.Tasks.Task<").Append(GuardResult)
                .Append("> ValidateAsync(").Append(t)
                .AppendLine(" value, global::System.Threading.CancellationToken cancellationToken = default)");
            sb.Append(indent).AppendLine("{");
            sb.Append(indent).AppendLine("    cancellationToken.ThrowIfCancellationRequested();");
            sb.Append(indent).Append("    return global::System.Threading.Tasks.Task.FromResult(Validate(value));");
            sb.AppendLine();
            sb.Append(indent).AppendLine("}");
        }

        private static void EmitProperty(StringBuilder sb, PropertyBinding property, string indent)
        {
            var schema = property.Schema;
            string member = "value." + property.MemberName;
            string errorKey = Escape(property.SchemaName);

            sb.Append(indent).Append("// Property: ").Append(property.SchemaName).AppendLine();

            // Required: the property key must be present. For a deserialized POCO a missing key surfaces
            // as null on a reference type, so required maps to a non-null check on reference members.
            if (property.Required && property.MemberIsReferenceType)
            {
                sb.Append(indent).Append("if (").Append(member).AppendLine(" is null)");
                AppendError(sb, indent + "    ", errorKey,
                    $"{property.SchemaName} is required.", "REQUIRED");
            }

            // The remaining constraints only make sense for a present value. Guard nullable/reference
            // members with a null check so a null value short-circuits to just the required rule above.
            bool needsNullGuard = property.MemberIsReferenceType;
            string checkIndent = indent;
            if (needsNullGuard)
            {
                sb.Append(indent).Append("if (").Append(member).AppendLine(" is not null)");
                sb.Append(indent).AppendLine("{");
                checkIndent = indent + "    ";
            }

            switch (property.Category)
            {
                case MemberTypeCategory.String:
                    EmitStringConstraints(sb, schema, member, errorKey, property.SchemaName, checkIndent);
                    EmitEnumConstraint(sb, schema, member, errorKey, property.SchemaName, checkIndent, quoteAsString: true);
                    break;

                case MemberTypeCategory.Numeric:
                    EmitNumericConstraints(sb, schema, member, errorKey, property.SchemaName, checkIndent,
                        property.MemberIsReferenceType);
                    EmitEnumConstraint(sb, schema, member, errorKey, property.SchemaName, checkIndent, quoteAsString: false);
                    break;

                case MemberTypeCategory.Collection:
                    EmitArrayConstraints(sb, schema, member, errorKey, property.SchemaName, checkIndent);
                    break;
            }

            if (needsNullGuard)
            {
                sb.Append(indent).AppendLine("}");
            }

            sb.AppendLine();
        }

        private static void EmitStringConstraints(
            StringBuilder sb, OpenApiSchema schema, string member, string errorKey, string name, string indent)
        {
            if (schema.MinLength is int minLength)
            {
                sb.Append(indent).Append("if (").Append(member).Append(".Length < ")
                    .Append(minLength.ToString(CultureInfo.InvariantCulture)).AppendLine(")");
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} must be at least {minLength} character(s) long.", "MIN_LENGTH");
            }

            if (schema.MaxLength is int maxLength)
            {
                sb.Append(indent).Append("if (").Append(member).Append(".Length > ")
                    .Append(maxLength.ToString(CultureInfo.InvariantCulture)).AppendLine(")");
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} must be at most {maxLength} character(s) long.", "MAX_LENGTH");
            }

            if (!string.IsNullOrEmpty(schema.Pattern))
            {
                string pattern = VerbatimPattern(schema.Pattern!);
                sb.Append(indent).Append("if (!").Append(Regex).Append(".IsMatch(").Append(member)
                    .Append(", @\"").Append(pattern).AppendLine("\"))");
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} does not match the required pattern.", "PATTERN");
            }

            EmitFormatConstraint(sb, schema, member, errorKey, name, indent);
        }

        private static void EmitFormatConstraint(
            StringBuilder sb, OpenApiSchema schema, string member, string errorKey, string name, string indent)
        {
            if (string.IsNullOrEmpty(schema.Format))
            {
                return;
            }

            // Each supported format maps to an anchored regex. The email/uuid/uri/date-time patterns mirror
            // the conventions used by the in-box OrionGuard.Generators email check; the remainder are
            // documented regexes. Unknown formats are ignored (an open-ended set in OpenAPI).
            string? formatPattern = schema.Format switch
            {
                "email" => @"^[^@\s]+@[^@\s]+\.[^@\s]+$",
                "uuid" => @"^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$",
                "date-time" => @"^\d{4}-\d{2}-\d{2}[Tt]\d{2}:\d{2}:\d{2}(\.\d+)?([Zz]|[+-]\d{2}:\d{2})?$",
                "date" => @"^\d{4}-\d{2}-\d{2}$",
                "uri" => @"^[A-Za-z][A-Za-z0-9+.-]*:.+$",
                "hostname" => @"^(?=.{1,253}$)([A-Za-z0-9]([A-Za-z0-9-]{0,61}[A-Za-z0-9])?)(\.[A-Za-z0-9]([A-Za-z0-9-]{0,61}[A-Za-z0-9])?)*$",
                "ipv4" => @"^((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)\.){3}(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)$",
                _ => null,
            };

            if (formatPattern is null)
            {
                return;
            }

            sb.Append(indent).Append("if (!").Append(Regex).Append(".IsMatch(").Append(member)
                .Append(", @\"").Append(formatPattern).AppendLine("\"))");
            AppendError(sb, indent + "    ", errorKey,
                $"{name} must be a valid {schema.Format} value.", "FORMAT");
        }

        private static void EmitNumericConstraints(
            StringBuilder sb, OpenApiSchema schema, string member, string errorKey, string name,
            string indent, bool memberIsNullable)
        {
            // For a Nullable<T> member that survived the null guard, read .Value so the comparison is
            // against the underlying numeric type.
            string operand = memberIsNullable ? member + ".Value" : member;

            if (schema.Minimum is string minimum)
            {
                string op = schema.ExclusiveMinimum ? "<=" : "<";
                string literal = NumericLiteral(minimum);
                sb.Append(indent).Append("if (").Append(operand).Append(' ').Append(op).Append(' ')
                    .Append(literal).AppendLine(")");
                string boundWord = schema.ExclusiveMinimum ? "greater than" : "greater than or equal to";
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} must be {boundWord} {minimum}.", "MINIMUM");
            }

            if (schema.Maximum is string maximum)
            {
                string op = schema.ExclusiveMaximum ? ">=" : ">";
                string literal = NumericLiteral(maximum);
                sb.Append(indent).Append("if (").Append(operand).Append(' ').Append(op).Append(' ')
                    .Append(literal).AppendLine(")");
                string boundWord = schema.ExclusiveMaximum ? "less than" : "less than or equal to";
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} must be {boundWord} {maximum}.", "MAXIMUM");
            }
        }

        private static void EmitArrayConstraints(
            StringBuilder sb, OpenApiSchema schema, string member, string errorKey, string name, string indent)
        {
            if (schema.MinItems is null && schema.MaxItems is null)
            {
                return;
            }

            // Count without assuming ICollection: a single Linq count covers arrays, lists, and any
            // IEnumerable. Cached into a local so min and max share one enumeration.
            sb.Append(indent).Append("var __count_").Append(SanitizeIdentifier(name)).Append(" = global::System.Linq.Enumerable.Count(")
                .Append("global::System.Linq.Enumerable.Cast<object>(").Append(member).AppendLine("));");
            string countVar = "__count_" + SanitizeIdentifier(name);

            if (schema.MinItems is int minItems)
            {
                sb.Append(indent).Append("if (").Append(countVar).Append(" < ")
                    .Append(minItems.ToString(CultureInfo.InvariantCulture)).AppendLine(")");
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} must contain at least {minItems} item(s).", "MIN_ITEMS");
            }

            if (schema.MaxItems is int maxItems)
            {
                sb.Append(indent).Append("if (").Append(countVar).Append(" > ")
                    .Append(maxItems.ToString(CultureInfo.InvariantCulture)).AppendLine(")");
                AppendError(sb, indent + "    ", errorKey,
                    $"{name} must contain at most {maxItems} item(s).", "MAX_ITEMS");
            }
        }

        private static void EmitEnumConstraint(
            StringBuilder sb, OpenApiSchema schema, string member, string errorKey, string name,
            string indent, bool quoteAsString)
        {
            if (schema.EnumValues.Count == 0)
            {
                return;
            }

            var rendered = new List<string>();
            foreach (var value in schema.EnumValues)
            {
                if (quoteAsString)
                {
                    string? s = value.AsString();
                    if (s is not null)
                    {
                        rendered.Add("\"" + Escape(s) + "\"");
                    }
                }
                else
                {
                    string? raw = value.RawNumber();
                    if (raw is not null)
                    {
                        rendered.Add(NumericLiteral(raw));
                    }
                }
            }

            if (rendered.Count == 0)
            {
                return;
            }

            sb.Append(indent).Append("if (");
            for (int i = 0; i < rendered.Count; i++)
            {
                if (i > 0)
                {
                    sb.Append(" && ");
                }

                if (quoteAsString)
                {
                    sb.Append("!global::System.String.Equals(").Append(member).Append(", ")
                        .Append(rendered[i]).Append(", global::System.StringComparison.Ordinal)");
                }
                else
                {
                    sb.Append(member).Append(" != ").Append(rendered[i]);
                }
            }

            sb.AppendLine(")");
            string allowed = string.Join(", ", EnumDisplay(schema, quoteAsString));
            AppendError(sb, indent + "    ", errorKey,
                $"{name} must be one of: {allowed}.", "ENUM");
        }

        private static IEnumerable<string> EnumDisplay(OpenApiSchema schema, bool quoteAsString)
        {
            foreach (var value in schema.EnumValues)
            {
                if (quoteAsString)
                {
                    if (value.AsString() is string s)
                    {
                        yield return s;
                    }
                }
                else if (value.RawNumber() is string raw)
                {
                    yield return raw;
                }
            }
        }

        private static void AppendError(StringBuilder sb, string indent, string key, string message, string code)
        {
            sb.Append(indent).Append("__errors.Add(new ").Append(ValidationError)
                .Append("(\"").Append(key).Append("\", \"").Append(Escape(message)).Append("\", \"")
                .Append(code).AppendLine("\"));");
        }

        /// <summary>
        /// Renders a numeric bound literal. A fractional or exponential bound becomes a <c>double</c>
        /// literal (suffixed <c>d</c>); an integral bound is emitted bare so it promotes against int, long,
        /// or decimal members without an explicit cast.
        /// </summary>
        private static string NumericLiteral(string raw)
        {
            if (raw.IndexOf('.') >= 0 || raw.IndexOf('e') >= 0 || raw.IndexOf('E') >= 0)
            {
                return raw + "d";
            }

            return raw;
        }

        private static string Escape(string value)
        {
            return value
                .Replace("\\", "\\\\")
                .Replace("\"", "\\\"")
                .Replace("\r", "\\r")
                .Replace("\n", "\\n");
        }

        /// <summary>Escapes a regex pattern for embedding in a C# verbatim string literal.</summary>
        private static string VerbatimPattern(string pattern) => pattern.Replace("\"", "\"\"");

        private static string SanitizeIdentifier(string value)
        {
            var sb = new StringBuilder(value.Length);
            foreach (char c in value)
            {
                sb.Append(char.IsLetterOrDigit(c) ? c : '_');
            }

            return sb.ToString();
        }
    }
}
